// Helper functions
export function getFeaturedScores(): Score[] {
  return scores.filter((score) => score.isFeatured);
}

export function getNewestScores(limit: number = 6): Score[] {
  return [...scores]
    .sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime())
    .slice(0, limit);
}

export function getMostPopularScores(limit: number = 6): Score[] {
  return [...scores]
    .sort((a, b) => b.downloadCount - a.downloadCount)
    .slice(0, limit);
}

export function getFeaturedComposers(): Composer[] {
  return composers.filter((composer) => composer.isFeatured);
}

export function getScoreBySlug(slug: string): Score | undefined {
  return scores.find((score) => score.slug === slug);
}

export function getComposerBySlug(slug: string): Composer | undefined {
  return composers.find((composer) => composer.slug === slug);
}

export function getScoresByComposer(composerId: string): Score[] {
  return scores.filter((score) => score.composerId === composerId);
}

export function getRelatedScores(scoreId: string, limit: number = 4): Score[] {
  const currentScore = scores.find((s) => s.id === scoreId);
  if (!currentScore) return [];
  
  return scores
    .filter(
      (s) =>
        s.id !== scoreId &&
        (s.composerId === currentScore.composerId ||
          s.categoryId === currentScore.categoryId)
    )
    .slice(0, limit);
}
